              function validateForm() {
                // This function deals with validation of the form fields
                // at least 1 checkbox must be checked
                let x = document.querySelectorAll("[data-stepcontent]");
                let checkboxes = x[currentTab].querySelectorAll(".check-block input[type='checkbox']");
                if (checkboxes.length > 0) {
                  let checkedOne = Array.prototype.slice.call(checkboxes).some(x => x.checked);
                  let y, i, valid = true;
                  if (!checkedOne) {
                    for (i = 0; i < checkboxes.length; i++) {
                      //  checkboxes[i].className += " error_input";
                    }
                    valid = false;
                  } else {
                    for (i = 0; i < checkboxes.length; i++) {
                      checkboxes[i].classList.remove("error_input");
                    }
                  }
                  // If the valid status is true, mark the step as finished and valid:
                  if (valid) {
                    document.querySelectorAll("[data-stepcontent]")[currentTab].className += " success";
                  }
                  return valid; // return the valid status
                } else {
                  let y, i, valid = true;
                  y = x[currentTab].querySelectorAll("input[required]");
                  // A loop that checks every input field in the current tab:
                  for (i = 0; i < y.length; i++) {
                    // If a field is empty...
                    if (y[i].value == "") {
                      // add an "invalid" class to the field:
                      y[i].className += " error_input";
                      // and set the current valid status to false:
                      valid = false;
                    } else {
                      y[i].classList.remove("error_input");
                    }
                  }
                  // If the valid status is true, mark the step as finished and valid:
                  if (valid) {
                    document.querySelectorAll("[data-stepcontent]")[currentTab].className += " success";
                  }
                  return valid; // return the valid status
                }
              }

              function fixStepIndicator(n) {
                // This function removes the "active" class of all steps...
